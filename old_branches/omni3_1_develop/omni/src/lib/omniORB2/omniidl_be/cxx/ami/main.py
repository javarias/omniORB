from omniidl_be.cxx.ami import hooks, ami

import main
self = main

# AMI code is generated by hooking into the normal backend code and
# adding new definitions.
def __init_hooks__():
    # Hook into existing IDL compiler code
    import omniidl_be.cxx.header.defs
    hooks.defs_visitInterface = omniidl_be.cxx.header.defs.visitInterface
    omniidl_be.cxx.header.defs.visitInterface = hooks.Interface_defs

    import omniidl_be.cxx.header.opers
    hooks.opers_visitInterface = omniidl_be.cxx.header.opers.visitInterface
    omniidl_be.cxx.header.opers.visitInterface = hooks.Interface_opers

    hooks.poa_visitInterface = omniidl_be.cxx.header.poa.visitInterface
    omniidl_be.cxx.header.poa.visitInterface = hooks.Interface_poa

    import omniidl_be.cxx.skel.main
    hooks.skel_visitInterface = omniidl_be.cxx.skel.main.visitInterface
    omniidl_be.cxx.skel.main.visitInterface = hooks.Implementation

    import omniidl_be.cxx.skel.poa
    hooks.skel_poa_visitInterface = omniidl_be.cxx.skel.poa.visitInterface
    omniidl_be.cxx.skel.poa.visitInterface = hooks.Implementation_poa



    

    

